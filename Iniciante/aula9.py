# OPERAÇÕES ARITMÉTICAS

adicao = 10 + 10
print('Adição: ', adicao)
print()

subtracao = 10 - 5
print('Subtração: ', subtracao)
print()

multiplicacao = 10 * 10
print('Multiplicação: ', multiplicacao)
print()

divisao = 10 / 2 #float
print('Divisão: ', divisao)
print()

divisao_inteira = 10 // 2 # Arredonda para cima
print('Divisão inteira: ', divisao_inteira) 
print()

exponenciacao = 2 ** 10 # 2 Elevado a 10
print('Exponenciação: ', exponenciacao)
print()

modulo = 55 % 2 #Resto da divisão
print('Módulo: ', modulo)
print()

