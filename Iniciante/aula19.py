'''
Operadores de comparação (relacionais)
OP      Significado     Exemplo(True)
>       maior           2 > 1 (True)
>=      maior ou igual  2 >= 1 (True)
<       menor           2 < 4 (True)
<=      menor ou igual  2 <= 4 (True)
==      igual           2 == 2 (True)
!=      diferente       2 != 4 (True)
'''

maior = 2 > 1
maior_igual = 2 >= 1
menor = 2 < 4
menor_igual  = 2 <= 4
igual = 2 == 2
diferente = 2 != 4

print(maior)
print(maior_igual)
print(menor)
print(menor_igual)
print(igual)
print(diferente)
