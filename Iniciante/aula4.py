# Tipos int e float
# int -> Números inteiros
# O tipo int representa todos e qualquer número positivo ou negativo
print(11)
print(-11)
print(0)
print()

# float -> Números flutuantes
# Números flutuantes são números com vírgulas, ou seja, com casas decimais
print(1.1, 10.11) #Usa-se ponto para float, vírgula é usada para outra coisa
print(0.0, 1.5)
print()

# A função type mostra o tipo que o Python inferiu o valor
print(type(1.6))
print(type('Olá'))
print(type(1))
print(type(True))
print()

